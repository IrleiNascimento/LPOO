    private void reset_digitacao() {
        this.texto_da_imagem.setText(null);
        this.texto_da_imagem.setEditable(true);
        this.texto_da_imagem.setBackground(Color.white);
        this.texto_da_imagem.setForeground(Color.black);
    }

    public void trocarImagem() {

        int palavras_count = getArrayPalavras().size();
        if (palavras_count > 0) {
            Random num = new Random();
            pselect = num.nextInt(palavras_count);
            imagem.setIcon(new ImageIcon(getClass().getResource("/imagens/" + getArrayPalavras().get(pselect) + ".jpg")));
            this.texto_da_imagem.grabFocus();

            this.botao_proximo.setVisible(false);
            reset_digitacao();
        }

    }

