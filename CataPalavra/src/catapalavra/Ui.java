    private void texto_da_imagemCaretUpdate(javax.swing.event.CaretEvent evt) {//GEN-FIRST:event_texto_da_imagemCaretUpdate
        // TODO add your handling code here:
        if (nomecorreto()) {
            texto_da_imagem.setForeground(Color.RED);
            texto_da_imagem.setEditable(false);
            texto_da_imagem.setBackground(Color.blue);
            pontos++;
            conta_pontos.setText(String.valueOf(pontos));
            this.botao_proximo.setVisible(true);
        } else {
            this.botao_proximo.setVisible(false);
        }

    }//GEN-LAST:event_texto_da_imagemCaretUpdate

