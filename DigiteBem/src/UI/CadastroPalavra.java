    private void salvar() {
        this.sessao.addPalavra(palavra);
        if (this.novo_tema != 0) {
            this.sessao.addTema((String) this.jComboBox_tema.getItemAt(this.jComboBox_tema.getItemCount() - 1));
            this.novo_tema = 0;
        }
        JOptionPane.showMessageDialog(null, "Pronto!");
        limpar();
    }

    private void limpar() {
        //this.jComboBox_nivel.setSelectedIndex(0);
        //this.jComboBox_tema.setSelectedIndex(0);
        this.jTextField_texto.setText(null);

    }

    private boolean validar() {
        String nivel = this.jComboBox_nivel.getSelectedItem().toString();
        String texto = this.jTextField_texto.getText();
        String tema = this.jComboBox_tema.getSelectedItem().toString();

        if (texto != null && !texto.equals("") && !nivel.equals("--") && !tema.equals("----")) {
            return true;
        } else {
            return false;
        }
    }

    public void popularcombTemas() {
        this.jComboBox_tema.addItem("----");
        for (String s : this.sessao.getTemas()) {
            this.jComboBox_tema.addItem(s);
        }

    }

    public AddTema getNovoTema() {
        if (this.novoTema == null) {
            this.novoTema = new AddTema(this.jComboBox_tema);
        }

        return novoTema;
    }
